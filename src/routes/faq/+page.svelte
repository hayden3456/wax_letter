                    <li>Addresses must be valid US addresses (we verify them)</li>
                    <li>Return address is required on all mail</li>
                    <li>Wax-sealed letters require extra postage (included in our pricing)</li>
                    <li>Certain items cannot be mailed (hazardous materials, etc.)</li>
                </ul>
            </div>

            <div class="faq-card">
                <h3>Is there a minimum order quantity?</h3>
