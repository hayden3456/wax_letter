            </div>

            <div class="faq-card">
                <h3>Can I make changes after placing an order?</h3>
                <p>It depends on where we are in the process. If we haven't started production yet, we can usually accommodate changes. Once printing or sealing has begun, changes may not be possible.</p>
                <p>Contact us as soon as possible if you need to make changes and we'll do our best to help.</p>
            </div>

            <div class="faq-card">
                <h3>Do you ship internationally?</h3>
